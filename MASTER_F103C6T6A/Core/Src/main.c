bool isBtnPushed(void)
{
	bool retVal = false;
	__disable_irq();
	retVal = (buttonIsPushed == true) ? true : false;
	buttonIsPushed = false;
	__enable_irq();
	return retVal;
}

/* Button PA0 ISR callback */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	static volatile uint32_t lastDebounceTime = 0UL;
	uint32_t currentTick = 0UL;

	if (GPIO_Pin == GPIO_PIN_0)
	{
		currentTick = HAL_GetTick();

		if ((currentTick - lastDebounceTime) > DEBOUNCE_DELAY)
		{
			buttonIsPushed = true;
			lastDebounceTime = currentTick;
		}
	}
}

